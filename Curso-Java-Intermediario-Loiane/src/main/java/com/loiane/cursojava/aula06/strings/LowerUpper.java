
package com.loiane.cursojava.aula06.strings;

/*
   Métodos toLowerCase - toUpperCase - transformando em maiúsculas e minúsculas
*/

public class LowerUpper {
    public static void main(String[]args){
    
        String teste = "Teste";
        
        // transforma toda frase em minúscula
        System.out.println(teste.toLowerCase());
        
        //transforma toda frase em maiúscula
        System.out.println(teste.toUpperCase());
        
    
    
    
}
}